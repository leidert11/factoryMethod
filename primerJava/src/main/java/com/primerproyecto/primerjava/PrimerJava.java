package com.primerproyecto.primerjava;

/**
 *
 * @author Usuario
 */
public class PrimerJava {
    public static void main(String[] args) {
        // Variables numéricas
        int numEntero = 10;
        double numDecimal = 3.14;
        float numFlotante = 2.5f;
        long numLargo = 1000000L;
        short numCorto = 5;
        byte numByte = 127;

        // Variables de texto
        String texto = "Hola, mundo!";

        // Variables booleanas
        boolean esVerdadero = true;
        boolean esFalso = false;

        // Variables de caracteres
        char caracter = 'A';

        // Variables de referencia
        Object objeto = new Object();

        // Imprimir los valores de las variables
        System.out.println("Número entero: " + numEntero);
        System.out.println("Número decimal: " + numDecimal);
        System.out.println("Número flotante: " + numFlotante);
        System.out.println("Número largo: " + numLargo);
        System.out.println("Número corto: " + numCorto);
        System.out.println("Número byte: " + numByte);
        System.out.println("Texto: " + texto);
        System.out.println("Es verdadero: " + esVerdadero);
        System.out.println("Es falso: " + esFalso);
        System.out.println("Caracter: " + caracter);
        System.out.println("Objeto: " + objeto);
    }
}
